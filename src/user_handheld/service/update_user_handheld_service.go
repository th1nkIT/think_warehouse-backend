func (s *UserHandheldService) UpdateUserHandheldIsActive(ctx context.Context, guid string) (err error) {
	// Get user handheld data
	data, err := s.GetUserHandheld(ctx, guid)
	if err != nil {
		return
	}

	tx, err := s.mainDB.BeginTx(ctx, &sql.TxOptions{})
	if err != nil {
		log.FromCtx(ctx).Error(err, "failed begin tx")
		err = errors.WithStack(httpservice.ErrUnknownSource)

		return
	}

	q := sqlc.New(s.mainDB).WithTx(tx)

	defer func() {
		if err != nil {
			if rollBackErr := tx.Rollback(); rollBackErr != nil {
				log.FromCtx(ctx).Error(err, "error rollback", rollBackErr)
				err = errors.WithStack(httpservice.ErrUnknownSource)

				return
			}
		}
	}()

	isActive := false
	if !data.IsActive.Bool {
		isActive = true
	}

	if err = q.UpdateUserHandheldIsActive(ctx, sqlc.UpdateUserHandheldIsActiveParams{
		IsActive: sql.NullBool{
			Bool:  isActive,
			Valid: true,
		},
		Guid: guid,
	}); err != nil {
		log.FromCtx(ctx).Error(err, "failed update user handheld is active")
		err = errors.WithStack(httpservice.ErrUnknownSource)

		return
	}

	if err = tx.Commit(); err != nil {
		log.FromCtx(ctx).Error(err, "error commit")
		err = errors.WithStack(httpservice.ErrUnknownSource)

		return
	}

	return
}
